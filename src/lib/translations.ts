export const localeMeta: Record<
  Locale,
  { htmlLang: string; dateLocale: string; shortLabel: string; label: string }
> = {
  es: { htmlLang: 'es', dateLocale: 'es-ES', shortLabel: 'ES', label: 'Español' },
  en: { htmlLang: 'en', dateLocale: 'en-US', shortLabel: 'EN', label: 'English' },
  pt: { htmlLang: 'pt', dateLocale: 'pt-PT', shortLabel: 'PT', label: 'Português' },
}

export function resolveLocale(value?: string | null): Locale {
  if (value?.startsWith('en')) return 'en'
  if (value?.startsWith('pt')) return 'pt'
  return 'es'
}

